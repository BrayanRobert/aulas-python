# Comando input() serve para pegar dados inseridos pelo usuário
input('Qual seu nome? ') # O usuário digita algo, mas seu valor não é salvo

idade = input('Qual sua idade? ') # Nesse caso, o valor que o usuário colocar será atribuido a variável idade

# concatenação de strings:
string1 = 'Ola, meu nome é '
string2 = 'Agenor'
string3 = string1 + string2 # Nova string criada a partir da junção de duas outras strings
print(string3)

# fstrings para inserir valor de uma variável em uma string
carro = 'honda civic'
print(f'Meu carro é um {carro}') # Coloque o f antes das aspas para atribuir o valor de uma variável em uma string passando a variavel entre chaves {}


