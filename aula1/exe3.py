# Peça para o usuário inserir a base e altura de um retângulo e imprima sua área
print("Para que a área de um retângulo possa ser calculada, defina os seguintes valores utilizando um número inteiro:")
base = int(input("base:"))
altura = int(input("altura"))
área = base * altura
print("A área desse retângulo é de", área, "m²")