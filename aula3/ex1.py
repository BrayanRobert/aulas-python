# Faça um programa que dê ao usuário 4 opções:
# 1 - Somar 2 + 2. 2 - Multiplicar 3 vezes 5, 3 - Mostra que é rio de janeiro, 4 - Sair do programa
valor = int(input("choose a number from 1 to 4 "))
if valor == 1:
    print(2+2)
elif valor == 2:
    print(3*5)
elif valor == 3:
    print("é rio de janeiro")